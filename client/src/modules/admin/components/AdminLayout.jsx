const NavItem = ({ item, onClick }) => {
  const { pathname } = useLocation();

  // Divider / section label
  if (item.divider) {
    return (
      <div className="px-3 pt-4 pb-1">
        <p className="text-[9px] font-medium tracking-widest uppercase text-white/20">{item.label}</p>
      </div>
    );
  }

  const active = item.exact ? pathname === item.to : pathname.startsWith(item.to);
  return (
    <Link to={item.to} onClick={onClick}
      className="flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-all duration-150"
      style={{
        background: active ? 'rgba(107,66,38,0.18)' : 'transparent',
        color:      active ? '#fff' : 'rgba(255,255,255,0.45)',
        border:     active ? '1px solid rgba(107,66,38,0.25)' : '1px solid transparent',
      }}>
      <span style={{ color: active ? '#8B5A3C' : 'rgba(255,255,255,0.3)' }}>{item.icon}</span>
      {item.label}
    </Link>
  );
};

